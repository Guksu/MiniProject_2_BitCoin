package org.min.BitCoin;

public class BitCoin{

	private int buyBitCoinVolume , bitCoinPrice;	
	
	public BitCoin() {
		
	}
	
	public int getBuyBitCoinVolume() {
		return buyBitCoinVolume;
	}

	public void setBuyBitCoinVolume(int buyBitCoinVolume) {
		this.buyBitCoinVolume = buyBitCoinVolume;
	}

	public int getBitCoinPrice() {
		return bitCoinPrice;
	}

	public void setBitCoinPrice(int bitCoinPrice) {
		this.bitCoinPrice = bitCoinPrice;
	}

}
